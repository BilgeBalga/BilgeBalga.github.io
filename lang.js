document.addEventListener("DOMContentLoaded", () => {
   const btnEn = document.getElementById("btn-en");
   const btnTr = document.getElementById("btn-tr");

   let currentLang = localStorage.getItem("preferredLanguage") || "en";

   function applyLanguage(lang) {
      if (lang === "tr") {
         btnTr.classList.add("is-active");
         btnEn.classList.remove("is-active");
      } else {
         btnEn.classList.add("is-active");
         btnTr.classList.remove("is-active");
      }

      document.querySelectorAll("[data-i18n]").forEach(el => {
         const key = el.getAttribute("data-i18n");
         if (translations[key] && translations[key][lang]) {
            el.innerHTML = translations[key][lang];
         }
      });

      document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
         const key = el.getAttribute("data-i18n-placeholder");
         if (translations[key] && translations[key][lang]) {
            el.setAttribute("placeholder", translations[key][lang]);
         }
      });

      localStorage.setItem("preferredLanguage", lang);
      document.documentElement.lang = lang;
   }

   if (btnEn && btnTr) {
      btnEn.addEventListener("click", () => applyLanguage("en"));
      btnTr.addEventListener("click", () => applyLanguage("tr"));
   }

   // Apply immediately on load
   applyLanguage(currentLang);
});
